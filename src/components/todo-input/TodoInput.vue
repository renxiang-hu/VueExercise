<template>
   <form class="form-inline" @submit.prevent="onFormSubmit">
     <div class="input-group mb-2 mr-sm-2">
       <div class="input-group-append">
         <div class="input-group-text">任务</div>
       </div>
       <input type="text" class="form-control" placeholder="请填写任务信息" style="width: 542px" v-model.trim="taskName"/>
     </div>
     <button type="submit" class="btn btn-primary mb-2">添加新任务</button>
   </form>
</template>

<script>
export default {
  name: "TodoInput",
  emits:['add'],
  methods:{
    onFormSubmit(){
      if (!this.taskName){
         return alert("任务名称不能为空");
      }
      this.$emit('add',this.taskName);
      this.taskName = '';
    }
  },
  data(){
    return{
      //新任务名称
      taskName:""
    }
  }
}
</script>

